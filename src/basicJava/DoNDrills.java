package basicJava;

public class DoNDrills {
    public static void main(String[] args) {
        for (int i=0 ; i<10 ; i++){
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=0 ; i<11 ; i++){
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=10 ; i>=0 ; i--){
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=1 ; i<33 ; i*=2){
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=1 ; i<26 ; i+=2){
            System.out.print(Math.sqrt(i) + " ");
        }
        System.out.println();
    }
}
